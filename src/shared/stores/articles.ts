export const useArticlesStore = defineStore('articles', () => {
  const articles = ref<Article[]>([...mockArticles])

  function setArticles(data: Article[]) {
    articles.value = data
  }

  function clearArticles() {
    articles.value = []
  }

  return {
    articles,
    setArticles,
    clearArticles,
  }
})
